def main():
    result = {}
    for hand, rgb in RAW.items():
        color_name = COLOR_NAMES.get(rgb, f"unknown_{rgb}")
        result[hand] = {"rgb": list(rgb), "color": color_name}

    out_dir = os.path.join(os.path.dirname(__file__), "..", "_materials")
    out_path = os.path.join(out_dir, "yokosawa_range_raw.json")
    with open(out_path, "w", encoding="utf-8") as f:
        json.dump(result, f, ensure_ascii=False, indent=2)
    print(f"出力完了: {out_path}")

    # 色名ごとの集計
    from collections import Counter
    counter = Counter(v["color"] for v in result.values())
    for color, count in counter.most_common():
        print(f"{color}: {count}ハンド")


if __name__ == "__main__":
    main()
